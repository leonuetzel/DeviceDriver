feedback DMAMUX::map(e_input input, e_output output)
{
	volatile uint32* channelAddress = MCU::DMAMUX::CH0_CR + (((uint32) output) & 0x0F);
	*channelAddress = ((uint32) input) & 0xFF;
	
	return(OK);
}